package classwork_16;

public class Classwork_16 {
	public static void main(String[] args) {
		byte a = 25;
		short b = a;
		int c = b;

		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		
		double d = 34.45;
		float f = (float) d;

		System.out.println(f);
		
		long l = 260L;
		byte b1 = (byte) l;

		System.out.println(b1);
	}
}

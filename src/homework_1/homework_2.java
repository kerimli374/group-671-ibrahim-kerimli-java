package homework_1;

public class homework_2 {
	public static void main(String[] args) {
//	    	Задание 1. Приветствие 10 раз
//			Напиши программу, которая 10 раз выводит на экран:  
//			"Привет, я учу Java!"  
//			Используй цикл for.
//				
		for (int i = 0; i < 10; i++) {
			System.out.println("Привет, я учу Java!");
		}

//			### Задание 2. Счёт от 1 до 20
//			Напиши программу, которая выводит числа от 1 до 20 по порядку (каждое число на новой строке).  
//			Используй цикл for.

		for (int i = 1; i <= 20; i++) {
			System.out.println(i);
		}
//
//			### Задание 3. Обратный отсчёт
//			Напиши программу, которая считает в обратном порядке от 10 до 1, а потом выводит "Пуск!".  
//			Используй цикл for.

		for (int i = 10; i >= 1; i--) {
			System.out.println(i);
		}
		System.out.println("Пуск");
//
//			### Задание 4. Таблица умножения на 5
//			Напиши программу, которая выводит таблицу умножения на 5:  
//			5 × 1 = 5  
//			5 × 2 = 10  
//			...  
//			5 × 10 = 50  
//			Используй любой цикл (for или while — на выбор).

		for (int i = 1; i <= 10; i++) {
			System.out.println(5 * i);
		}
//
//			### Задание 5. Повторяем, пока не надоест
//			Напиши программу с бесконечным циклом, которая всё время выводит:  
//			"Я люблю программировать!"  
//			Используй for (;;).  
//			(Чтобы остановить программу, нажми красную кнопку Stop в среде разработки.)
//				
//				for (;;) {
//					System.out.println("Я люблю программировать!");
//				}

//			### Задание 6. Любимые животные
//			Создай массив из 5 своих любимых животных, например:  

		String[] animals = { "кот", "собака", "хомяк", "попугай", "кролик" };

//				
//			Напиши цикл for, который выведет:  
//			Животное 1: кот  
//			Животное 2: собака  
//			и так далее.	
		int j = 1;
		for (int i = 0; i < 2; i++) {
			System.out.println("Животное " + j + ": " + animals[i]);
			j++;
		}
//
//			### Задание 7. Последний фрукт
//			Создай массив фруктов:  
//			String[] fruits = {"яблоко", "банан", "груша", "апельсин", "киви"};  
//
		String[] fruits = { "яблоко", "банан", "груша", "апельсин", "киви" };

//			Выведи на экран только последний фрукт из списка (без цикла — просто по индексу).
//				
		System.out.println(fruits[4]);

//			### Задание 8. Готовые и неготовые пирожки
//			Есть массив пирожков: 1 — готов, 0 — ещё сырой.  

		int[] pies = { 1, 0, 1, 1, 0, 1, 0, 1 };
//
//			Напиши цикл for, который для каждого пирожка выводит:  
//			"Пирожок готов!" — если 1  
//			и ничего не выводит — если 0 (используй continue).

		for (int i = 0; i <= 7; i++) {
			if (pies[i] == 1) {
				System.out.println("Пирожок готов!");
			} else {
				continue;
			}
		}

//
//			### Задание 9. Считаем до 100 пока маленькое
//			Напиши программу с переменной int number = 1;
//			Используй цикл do-while, чтобы выводить число и увеличивать его на 1, пока оно меньше или равно 10.  
//			(Даже если изначально задать number = 15, цикл выполнится хотя бы один раз.)
//
		int number = 9;
		do {
			System.out.println(number);
			number++;
		} while (number <= 10);

//			### Задание 10. Пропускаем чётные числа
//			Напиши цикл for от 1 до 20.  
//			Если число чётное — пропусти его (continue).  
//			Если нечётное — выведи: "Нечётное число: 1", "Нечётное число: 3" и т.д.

		for (int i = 1; i < 20; i++) {
			if (i % 2 != 0) {
				System.out.println("Нечётное число:" + i);
			} else {
				continue;
			}
		}

	}
}
